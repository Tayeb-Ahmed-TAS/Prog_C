#include <stdio.h>

int main()

{

    int a = 3, b = 22;

    printf("a = %d\nb = %d\n", a, b);

    printf("~ a is : %d\n", ~a);

    printf("a & b is : %d\n", a & b);

    printf("a | b is : %d\n", a | b);

    printf("a ^ b is : %d\n", a ^ b);

    printf("a <<= 3 is : %d\n", a <<= 3);

    printf("a >>= 3 is : %d\n", a >>= 3);
}